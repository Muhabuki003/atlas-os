def test_reports_for_queue_buckets(atlas_data_dir):
    today = atlas_agents._today_utc()
    atlas_config.save_reports([
        {"id": "1", "status": "waiting_for_review", "created_at": f"{today}T10:00:00+00:00"},
        {"id": "2", "status": "approved", "created_at": f"{today}T11:00:00+00:00"},
        {"id": "3", "status": "revision_requested", "created_at": f"{today}T09:00:00+00:00"},
    ])
    queue = atlas_agents.reports_for_queue()
    assert len(queue["waiting_for_approval"]) == 1
    assert len(queue["completed_today"]) == 1
    assert len(queue["pending"]) == 1
